<script lang="ts">
  import { onMount, onDestroy } from 'svelte';

  let { symbol = 'COINBASE:ETHUSD' } = $props();
  let widgetContainer: HTMLDivElement | null = null;
  const URL = import.meta.env.VITE_TRADING_VIEW_LINK;

  const initializeWidget = () => {
    try {
      if (!widgetContainer) return;

      // Clear any existing widget
      widgetContainer.innerHTML = '';

      // Add the widget container for the new TradingView script
      const script = document.createElement('script');
      script.type = 'text/javascript';
      script.async = true;
      script.src = '../../js/embed-widget-symbol-info.js';
      script.innerHTML = JSON.stringify({
        width: '392',
        isTransparent: false,
        symbol, // Dynamically set the symbol
        locale: 'en',
        colorTheme: 'light',
      });

      widgetContainer.appendChild(script);
    } catch (error) {
      console.log('Error initializing TradingView widget', error);
    }

  };

  // Reactively reinitialize the widget when `symbol` changes
  $effect(() => {
    initializeWidget();
  });

  onMount(() => {
    initializeWidget();
  });

  onDestroy(() => {
    if (widgetContainer) {
      widgetContainer.innerHTML = '';
    }
  });
</script>

<div class="tradingview-widget-container">
  <div bind:this={widgetContainer} class="tradingview-widget-container__widget"></div>
  <div class="tradingview-widget-copyright">
    <a
      href={URL}
      rel="noopener nofollow"
      target="_blank"
    >
      <span class="blue-text">Track all markets on TradingView</span>
    </a>
  </div>
</div>

